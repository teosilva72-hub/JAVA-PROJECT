//RESET MESSAGE DISPLAY	
function hideSuccessMessage() {
    setTimeout(function () {
        $('#success').hide();
    }, 5000);
}

//SHOW MESSAGE DISPLAY	
function showSuccessMessage() {
    $('#success').show();
}

